	<article class="history-article">
		<h3>Las puertas de madera <br> tienen apellido</h3>
		<h4>1890-1920 – Ramón Zubillaga</h4>
		<p>Eran épocas de <strong> herramientas manuales y nobles maderas macizas</strong>
			(roble eslavonia, cedro paraguayo) que se desembarcaban en el
			antiguo puerto de Mar del Plata y se transportaban en carretones
			hasta el taller. Fue en el año 1891 cuando <strong> Ramón Zubillaga </strong>instaló
			su taller de carpintería en La Rioja al 2100 (hoy centro), Mar del Plata.
			En aquel primer edificio comenzó a elaborar en forma artesanal, carpintería para
			importantísimos y típicos <strong> chalets marplatenses.</strong></p>
	</article>
	<article class="history-article-hnos">
		<h4>1920-1960 – Los Seis Zetas</h4>
		<p>Casado con María Durquet, sus cinco hijos hicieron eco de su actividad.
			La <strong> innovación permanente </strong> fue a la vez que <strong> pasión, oficio.</strong> En 1920 se los conoció por su
			marca <strong> 6 Zetas</strong> (seis Zubillaga). Fundaron un pueblo en Quillén (Río Negro). Allí maquinaron la
			madera que luego trajeron a Mar del Plata. Del quehacer cotidiano sobrevino la invención: fueron
			creadores de la primera y revolucionaria <strong> puerta placa</strong>, que reemplazaría la típica puerta maciza
			de tablero. Elaboraron también el primer <strong>tablero de madera maciza </strong> para fabricar muebles. Los combinados
			Philco, los muebles de <strong>Mapple</strong> y los gabinetes de las máquinas de coser Singer, entre otros, utilizaron la
			placa maciza 6 Zetas, que se impuso como un producto de <strong>alta innovación</strong> en la época.</p>
	</article>
	<article class="history-article-raul">
		<h4>1960-2000 – Raúl Zubillaga</h4>
		<p>
			Hacia 1970 llegó la tercera generación.
			<strong>Raúl Zubillaga</strong> instaló una moderna <strong>planta industrial</strong> en San Salvador y Ratery.
			Allí se inició la modernización: se observó el <strong>confort y la seguridad de los trabajadores</strong>
			y las <strong>nuevas tendencias mundiales</strong> se abrieron paso. Se perfeccionaron conceptos técnicos para
			la fabricación de <strong>puertas placa enchapadas</strong> de muy alta calidad y a nivel masivo. Aquellas nuevas
			puertas placa 6 Zetas, siguen jerarquizando hoy, los edificios más importantes de la ciudad.
		</p>
	</article>
	<article class="history-article-diego">
		<h4>2000-actualidad – Diego Zubillaga</h4>
		<p>A mediados de los noventa se sumó la cuarta generación con el<strong> Arq. Diego Zubillaga.</strong> La empresa logró su máximo nivel
			de precisión tecnológica para la fabricación de puertas de alta calidad.
			La participación de <strong> Pablo</strong>, y el paso de <strong>Hernán, Luciano y Laura Zubillaga</strong> incrementaron la capacidad de respuesta a
			diseñadores de primera línea.
			En 2000, <strong>Zubillaga Empresa de Carpintería es condecorada</strong> por la Handwerkskammer Rheinhessen en la ciudad de <strong>Mainz,
				Alemania</strong>, por su notable desarrollo empresarial.
			En 2012 se inaugura la nueva planta industrial, proyectada especialmente para la producción de sofisticadas aberturas
			interiores e madera.
			En la actualidad, <strong>ZUBILLAGA PUERTAS NOBLES</strong> realiza una permanente capacitación de su personal, infundiendo el espíritu
			y las técnicas que hacen a la marca.
			Sigue incorporando tecnología electrónica de punta que actualiza su crecimiento sostenido.
			Sigue abasteciendo la construcción horizontal y unifamiliar de alta gama con puertas de vanguardia.
			Se ha posicionado como <strong>REFERENTE INDISCUTIBLE</strong> de calidad e innovación en la fabricación de <strong>PUERTAS INTERIORES DE CALIDAD.</strong>
		</p>
